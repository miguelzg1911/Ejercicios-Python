# Pide dos edades y muestra si alguna es menor que 18 y la otra mayor que 60.

edad1 = int(input("Ingresa una edad: "))
edad2 = int(input("Ingresa otra edad: "))

if edad1 < 18:
    print("La edad 1 es menor de 18")
elif edad1 > 60:
    print("La edad 1 es mayor de 60")
if edad2 < 18:
    print("La edad 2 es menor de 18")
elif edad2 > 60:
    print("La edad 2 es mayor de 60")

